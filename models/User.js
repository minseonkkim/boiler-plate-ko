const mongoose = require('mongoose')
const Schema = mongoose.Schema;

const productSchema = mongoose.Schema({
	name: {
        type: String,
        maxlength: 50
    },
    email: {
        type: String,
        trim: true,
        unique: 1
    },
    password: {
        type: String,
        minlength: 5,
    },
    lastname: {
        type: String,
        maxlength: 50
    },
    role: {
        type: Number,
        default: 0
    },
    image: String,
    token: {
        type: String
    },
    tokenExp: {
        type: Number
    }
}, { timestamps: true })

const Product = mongoose.model('Product', productSchema);

module.exports = { Product }
